#!/bin/bash
check_root() {
# $EUID — эффективный ID пользователя
# 0 — это ID суперпользователя (root)
if [ $EUID -eq 0 ]; then
echo "✓ Скрипт запущен от имени root (UID: $EUID)"
return 0  # Успех
else
echo "✗ ОШИБКА: Скрипт должен быть запущен от имени суперпользователя!"
echo "Текущий UID: $EUID"
echo "Попробуйте: sudo $0"
exit 1
fi
}
echo "=== Проверка прав доступа ==="
echo ""
# Вызов функции проверки
check_root
echo ""
echo "=== Скрипт выполняется с правами root ==="
echo "Вы можете выполнять административные действия:"
echo "- Создавать пользователей"
echo "- Устанавливать пакеты"
echo "- Изменять системные файлы"
echo ""
