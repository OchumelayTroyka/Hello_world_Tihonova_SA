#!/bin/bash
# Скрипт "Числовой фильтр"
# Перебирает числа от 1 до 20, выводит нечетные, останавливается на 15
echo "=== Числовой фильтр ==="
echo "Вывод нечетных чисел от 1 до 20 (остановка на 15):"
echo ""
for i in {1..20}; do
# Проверка на число 15 - останавливаем цикл
if [ $i -eq 15 ]; then
echo "Достигнуто число 15. Прекращаем выполнение."
break
fi
# Проверка на четность - если четное, пропускаем
if [ $((i % 2)) -eq 0 ]; then
continue
fi
# Вывод нечетного числа
echo "Нечетное число: $i"
done
echo ""
echo "Цикл завершен."
